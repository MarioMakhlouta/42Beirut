int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	n = nb;
	i = 2;
	if (n <= 0)
	{
		return (0);
	}
	if (n == 1)
	{
		return (1);
	}
	if (n >= 2)
	{
		while (i * i <= n)
		{
			if (i * i == n)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
